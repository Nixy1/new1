function calculate(a,b,d)
{
    if(d=="+")
    {
        sum=a+b
        console.log("sum=",sum)
    }
    
    else if(d=="-")
    {
        sub=a-b
        console.log("sub=",sub)
    }
    
    else if(d="/")
    {
        div=a/b
        console.log("div=",div)
    }
    
    else if(d="*"){
        mul=a*b
        console.log("mul=",mul)
    }
}
calculate(10,20,"+")
calculate(30,40,"-")
calculate(50,60,"/")
calculate(70,80,"*")








        
        
        
        
        
        
        
        
   
